#!/usr/bin/env node
// Generate supabase/migrations/122_eps_de1_de2_seed.sql from real static data
// (src/data/eps_de1.ts, eps_de2.ts).
//
// Why: Đề 1 và Đề 2 là đề thật, mất 10 ngày soạn. Đem lên Supabase để admin
// quản lý qua /admin/eps-exam-manager (sửa, đổi tên), nhưng phải khớp 100%
// với những gì học viên đang thấy → script này import trực tiếp file TS,
// không gõ tay câu nào.
//
// Idempotent: dùng deterministic UUID cho 2 exam + upsert. Chạy lại an toàn.

import { createJiti } from "jiti";
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const OUT = path.join(ROOT, "supabase/migrations/122_eps_de1_de2_seed.sql");

const jiti = createJiti(import.meta.url, { interopDefault: true });
const de1 = await jiti.import(path.join(ROOT, "src/data/eps_de1.ts"));
const de2 = await jiti.import(path.join(ROOT, "src/data/eps_de2.ts"));

// ─── Deterministic UUIDs (đừng đổi — re-run depends on these) ───────
const EXAM_UUIDS = {
  de1: "11111111-de01-4000-8000-000000000001",
  de2: "11111111-de02-4000-8000-000000000002",
};

// ─── SQL escaping ───────────────────────────────────────────────────
const sqlStr = (v) => {
  if (v == null || v === "") return "NULL";
  return `'${String(v).replace(/'/g, "''")}'`;
};
const sqlInt = (v) => (v == null ? "NULL" : String(parseInt(v, 10)));
const sqlBool = (v) => (v ? "true" : "false");
const sqlJsonb = (v) => {
  if (v == null) return "NULL";
  return `'${JSON.stringify(v).replace(/'/g, "''")}'::jsonb`;
};

// ─── Build exam row ─────────────────────────────────────────────────
function examInsert(info, slug, examNo, uuid) {
  const title = info.title;
  const totalQ = info.totalQuestions ?? 40;
  const timeM = info.timeMinutes ?? 50;
  const description = `Đề thi EPS-TOPIK thật (40 câu — ${info.readingCount ?? info.readingQuestions ?? 20} đọc + ${info.listeningCount ?? info.listeningQuestions ?? 20} nghe). Có audio + giải thích chi tiết.`;
  return `insert into public.eps_exams (id, slug, title, year, exam_no, description, total_questions, time_minutes, is_published)
values (${sqlStr(uuid)}::uuid, ${sqlStr(slug)}, ${sqlStr(title)}, 2025, ${examNo}, ${sqlStr(description)}, ${totalQ}, ${timeM}, ${sqlBool(true)})
on conflict (slug) do update set
  title = excluded.title,
  description = excluded.description,
  total_questions = excluded.total_questions,
  time_minutes = excluded.time_minutes,
  is_published = excluded.is_published,
  updated_at = now();`;
}

// ─── Build question row ─────────────────────────────────────────────
function questionInsert(examUuid, q, explanation) {
  // question_type fits CHECK ('listening','reading','image'):
  // - listening section → 'listening'
  // - reading section, option_type=image → 'image'
  // - reading section, option_type=text  → 'reading'
  const questionType =
    q.section === "listening"
      ? "listening"
      : q.optionType === "image"
        ? "image"
        : "reading";

  const correctAnswer = (q.correct ?? 0) + 1; // 0-based → 1-based
  const cols = [
    "exam_id",
    "order_no",
    "question_type",
    "section",
    "option_type",
    "question_text",
    "content",
    "content_image",
    "options",
    "option_images",
    "audio_script",
    "audio_options",
    "audio_hint",
    "explanation",
    "correct_answer",
  ];
  const vals = [
    `${sqlStr(examUuid)}::uuid`,
    sqlInt(q.id),
    sqlStr(questionType),
    sqlStr(q.section),
    sqlStr(q.optionType),
    sqlStr(q.prompt),
    sqlStr(q.content),
    sqlStr(q.contentImage),
    sqlJsonb(q.options),
    sqlJsonb(q.optionImages ?? null),
    sqlStr(q.audioScript),
    sqlJsonb(q.audioOptions ?? null),
    sqlStr(q.audioHint),
    sqlStr(explanation),
    sqlInt(correctAnswer),
  ];
  return `insert into public.eps_questions (${cols.join(", ")})
values (${vals.join(", ")})
on conflict (exam_id, order_no) do update set
  question_type = excluded.question_type,
  section = excluded.section,
  option_type = excluded.option_type,
  question_text = excluded.question_text,
  content = excluded.content,
  content_image = excluded.content_image,
  options = excluded.options,
  option_images = excluded.option_images,
  audio_script = excluded.audio_script,
  audio_options = excluded.audio_options,
  audio_hint = excluded.audio_hint,
  explanation = excluded.explanation,
  correct_answer = excluded.correct_answer;`;
}

// ─── Render ─────────────────────────────────────────────────────────
const lines = [];
lines.push(`-- Migration: Seed Đề 1 + Đề 2 EPS-TOPIK vào DB (idempotent).`);
lines.push(`-- Auto-generated by scripts/gen-eps-exam-seed.mjs from`);
lines.push(`--   src/data/eps_de1.ts (DE1_QUESTIONS + DE1_EXPLANATIONS + DE1_INFO)`);
lines.push(`--   src/data/eps_de2.ts (DE2_QUESTIONS + DE2_EXPLANATIONS + DE2_INFO)`);
lines.push(`-- Re-run safe: dùng deterministic UUID + upsert on conflict.`);
lines.push(`-- Đừng sửa file này thủ công — chạy lại script khi đề tĩnh đổi.`);
lines.push(``);
lines.push(`begin;`);
lines.push(``);
lines.push(`-- ── Đề 1 ─────────────────────────────────────────────`);
lines.push(examInsert(de1.DE1_INFO, "eps-de-1", 1, EXAM_UUIDS.de1));
lines.push(``);
for (const q of de1.DE1_QUESTIONS) {
  lines.push(questionInsert(EXAM_UUIDS.de1, q, de1.DE1_EXPLANATIONS?.[q.id]));
}
lines.push(``);
lines.push(`-- ── Đề 2 ─────────────────────────────────────────────`);
lines.push(examInsert(de2.DE2_INFO, "eps-de-2", 2, EXAM_UUIDS.de2));
lines.push(``);
for (const q of de2.DE2_QUESTIONS) {
  lines.push(questionInsert(EXAM_UUIDS.de2, q, de2.DE2_EXPLANATIONS?.[q.id]));
}
lines.push(``);
lines.push(`commit;`);
lines.push(``);

writeFileSync(OUT, lines.join("\n"), "utf8");
console.log(`✔ Wrote ${OUT}`);
console.log(`  Đề 1: ${de1.DE1_QUESTIONS.length} câu`);
console.log(`  Đề 2: ${de2.DE2_QUESTIONS.length} câu`);
