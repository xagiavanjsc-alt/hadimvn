// Build templates.ts cho Edge Function send-email
// Chạy trước khi deploy: node scripts/build-edge-function.mjs
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.join(__dirname, "..");
const templatesDir = path.join(rootDir, "docs", "email-templates");
const outputFile = path.join(rootDir, "supabase", "functions", "send-email", "templates.ts");

const TEMPLATE_MAP = {
  welcome: { file: "welcome.html", subject: "Chào mừng đến với Hàn Quốc Ơi!" },
  "vip-success": { file: "vip-success.html", subject: "Kích hoạt VIP thành công — Hàn Quốc Ơi!" },
  "vip-expiring": { file: "vip-expiring.html", subject: "VIP sắp hết hạn — Gia hạn ngay để không gián đoạn" },
  "streak-warning": { file: "streak-warning.html", subject: "🔥 Đừng để mất streak — chỉ 5 phút thôi!" },
  "streak-milestone": { file: "streak-milestone.html", subject: "🏆 Chúc mừng đạt cột mốc mới!" },
  "weekly-summary": { file: "weekly-summary.html", subject: "📊 Tổng kết tuần học tập của bạn" },
  "leaderboard-up": { file: "leaderboard-up.html", subject: "🚀 Bạn vừa lên hạng!" },
  "daily-reminder": { file: "daily-reminder.html", subject: "☀️ Nhắc học tiếng Hàn hôm nay" },
  "coupon-received": { file: "coupon-received.html", subject: "🎁 Bạn nhận được mã giảm giá mới!" },
  "feedback-reply": { file: "feedback-reply.html", subject: "💬 Admin đã phản hồi góp ý của bạn" },
};

function escapeTemplate(html) {
  return html.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
}

const entries = [];
for (const [key, { file, subject }] of Object.entries(TEMPLATE_MAP)) {
  const html = fs.readFileSync(path.join(templatesDir, file), "utf8");
  const escaped = escapeTemplate(html);
  entries.push(`  "${key}": {\n    subject: ${JSON.stringify(subject)},\n    html: \`${escaped}\`,\n  }`);
  console.log(`Loaded: ${file}`);
}

const content = `// AUTO-GENERATED by scripts/build-edge-function.mjs — DO NOT EDIT
// Regenerate: node scripts/build-edge-function.mjs

export interface EmailTemplate {
  subject: string;
  html: string;
}

export const TEMPLATES: Record<string, EmailTemplate> = {
${entries.join(",\n")},
};
`;

fs.writeFileSync(outputFile, content, "utf8");
console.log(`\n✓ Generated: ${path.relative(rootDir, outputFile)}`);
console.log("  Next: supabase functions deploy send-email --no-verify-jwt");
