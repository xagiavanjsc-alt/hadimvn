#!/usr/bin/env node
// Generate supabase/migrations/017a_eps_vocab_seed.sql from src/mocks/epsVocabulary.ts.
// One-shot. Idempotent (ON CONFLICT DO NOTHING) so re-running it on prod is safe.
//
// Parses the .ts file as text (the data is a literal object array with no dynamic
// imports), then re-emits as SQL INSERT VALUES with proper escaping.

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const SRC = path.join(ROOT, "src/mocks/epsVocabulary.ts");
const OUT = path.join(ROOT, "supabase/migrations/017a_eps_vocab_seed.sql");

const text = readFileSync(SRC, "utf8");

// ─── Parse TOPICS array ─────────────────────────────────────────────────────
function parseObjectsBlock(label) {
  const re = new RegExp(`export const ${label}[^=]*=\\s*\\[([\\s\\S]*?)\\];`, "m");
  const m = text.match(re);
  if (!m) throw new Error(`Could not locate ${label}`);
  return m[1];
}

// Generic top-level object matcher inside a [ ... ] block.
// Handles nested braces in case any future entry contains one.
function splitTopLevelObjects(block) {
  const out = [];
  let depth = 0;
  let start = -1;
  let inStr = null;
  for (let i = 0; i < block.length; i++) {
    const c = block[i];
    const prev = block[i - 1];
    if (inStr) {
      if (c === inStr && prev !== "\\") inStr = null;
      continue;
    }
    if (c === '"' || c === "'" || c === "`") { inStr = c; continue; }
    if (c === "{") { if (depth === 0) start = i; depth++; }
    else if (c === "}") { depth--; if (depth === 0 && start !== -1) { out.push(block.slice(start, i + 1)); start = -1; } }
  }
  return out;
}

// Extract a key from an object literal like `{ id: "ev001", korean: "안녕", ... }`.
// Supports double-quoted, single-quoted, and template-string values without
// interpolation. Returns undefined when the key is missing.
function readField(obj, key) {
  const re = new RegExp(`\\b${key}\\s*:\\s*("([^"\\\\]|\\\\.)*"|'([^'\\\\]|\\\\.)*'|\`([^\`\\\\]|\\\\.)*\`)`);
  const m = obj.match(re);
  if (!m) return undefined;
  const lit = m[1];
  // strip surrounding quote, then unescape \" \' \\ \n etc.
  const inner = lit.slice(1, -1);
  return inner
    .replace(/\\"/g, '"')
    .replace(/\\'/g, "'")
    .replace(/\\`/g, "`")
    .replace(/\\\\/g, "\\")
    .replace(/\\n/g, "\n")
    .replace(/\\t/g, "\t");
}

function sqlQuote(s) {
  if (s === undefined || s === null) return "NULL";
  return "'" + String(s).replace(/'/g, "''") + "'";
}

// ─── TOPICS ─────────────────────────────────────────────────────────────────
const topicsRaw = splitTopLevelObjects(parseObjectsBlock("EPS_VOCAB_TOPICS"));
const topics = topicsRaw.map((obj, idx) => ({
  id: readField(obj, "id"),
  label: readField(obj, "label"),
  label_ko: readField(obj, "labelKo"),
  icon: readField(obj, "icon"),
  color: readField(obj, "color"),
  description: readField(obj, "description"),
  sort_order: idx,
}));

// ─── ENTRIES ────────────────────────────────────────────────────────────────
const entriesRaw = splitTopLevelObjects(parseObjectsBlock("epsVocabulary"));
const entries = entriesRaw.map(obj => ({
  id: readField(obj, "id"),
  korean: readField(obj, "korean"),
  reading: readField(obj, "reading"),
  vietnamese: readField(obj, "vietnamese"),
  example: readField(obj, "example"),
  example_vi: readField(obj, "exampleVi"),
  topic_id: readField(obj, "topicId"),
  level: readField(obj, "level") ?? "basic",
}));

// Sanity checks — fail loud if parse missed something.
const badTopics = topics.filter(t => !t.id || !t.label);
const badEntries = entries.filter(e => !e.id || !e.korean || !e.vietnamese);
if (badTopics.length > 0) throw new Error(`Bad topics: ${badTopics.length} rows missing id/label`);
if (badEntries.length > 0) throw new Error(`Bad entries: ${badEntries.length} rows missing id/korean/vietnamese`);

// ─── Emit SQL ───────────────────────────────────────────────────────────────
const header = `-- 017a_eps_vocab_seed.sql
-- AUTO-GENERATED by scripts/gen-eps-seed.mjs from src/mocks/epsVocabulary.ts.
-- Idempotent (ON CONFLICT DO NOTHING) — safe to re-run.
-- Source rows: ${topics.length} topics, ${entries.length} entries.

`;

const topicsSql =
  `INSERT INTO public.eps_vocab_topics (id, label, label_ko, icon, color, description, sort_order) VALUES\n` +
  topics.map(t =>
    `  (${sqlQuote(t.id)}, ${sqlQuote(t.label)}, ${sqlQuote(t.label_ko)}, ${sqlQuote(t.icon)}, ${sqlQuote(t.color)}, ${sqlQuote(t.description)}, ${t.sort_order})`
  ).join(",\n") +
  `\nON CONFLICT (id) DO NOTHING;\n\n`;

const entriesSql =
  `INSERT INTO public.eps_vocab_entries (id, korean, reading, vietnamese, example, example_vi, topic_id, level) VALUES\n` +
  entries.map(e =>
    `  (${sqlQuote(e.id)}, ${sqlQuote(e.korean)}, ${sqlQuote(e.reading)}, ${sqlQuote(e.vietnamese)}, ${sqlQuote(e.example)}, ${sqlQuote(e.example_vi)}, ${sqlQuote(e.topic_id)}, ${sqlQuote(e.level)})`
  ).join(",\n") +
  `\nON CONFLICT (id) DO NOTHING;\n`;

writeFileSync(OUT, header + topicsSql + entriesSql, "utf8");
console.log(`✓ Wrote ${OUT}`);
console.log(`  ${topics.length} topics, ${entries.length} entries`);
